package com.koreait.first.ch02;

public class For {
    /*

        for( A ; B ; C ) {

        }
        A: 초기화
        B: 비교 (계속 돌까?)
        C: 증감식
     */
    public static void main(String[] args) {

        for(int i=0; i<5; i++) {
            System.out.println(i);
        }

        for(int i=0; i<5; i++) {
            System.out.println(i);
        }

        System.out.println("종료");
    }
}

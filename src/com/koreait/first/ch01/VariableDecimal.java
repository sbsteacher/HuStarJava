package com.koreait.first.ch01;

public class VariableDecimal {
    // 변수 : 값을 담을 수 있고, 값이 변할 수 있는 공간

    //정수형 변수 (소수점이 없는 숫자)
    public static void main(String[] args) {
        //변수 선언 : 변수를 사용하고 싶으면 선언부터!!!
        byte b; //byte 타입, b 변수명, byte 타입의 변수명 b를 선언했다.

        //처음 값을 주입할때 초기화라고 부른다.
        b = 11; // = 은 오른쪽에 있는 값을 복사하여 왼쪽에 대입한다.
        System.out.println(b);

        short s;
        int i = 10, i2 = 20, i3, i4 = 100;
        long l;
    }
}

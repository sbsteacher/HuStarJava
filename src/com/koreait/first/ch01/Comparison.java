package com.koreait.first.ch01;

public class Comparison {
    public static void main(String[] args) {
        int n1 = 10;
        int n2 = 10;
        System.out.println("n1 > n2 : " + (n1 > n2));
        System.out.println("n1 >= n2 : " + (n1 >= n2));
        System.out.println("n1 <= n2 : " + (n1 <= n2));
        System.out.println("n1 < n2 : " + (n1 < n2));
        System.out.println("n1 == n2 : " + (n1 == n2));
    }
}
